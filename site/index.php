        const datesForDisable = [];
        var allowTimes = ['9:00', '10:00', '11:00', '12:00', '13:00', '14:00', '15:00', '16:00', '17:00', '18:00', '19:00', '20:00', '21:00'];

        $(function() {
            $("input[name='datetimes']").datetimepicker({
                format: "d.m.Y H:m",
                minDate: new Date,
                minTime: new Date,
                allowTimes: allowTimes,
                disabledDates: datesForDisable,
                onSelectDate: function(ct, $i) {
                    item = this;
                    $.ajax({
                        url: "checkTime.php",
                        data: {'date' : dateFormat(ct, "d.mm.yyyy")},
                        method: "POST",
                        context: document.body,
                        success: function (res) {
                            if(res) {
                                allowTimes = allowTimes.filter(x => !res.includes(x));                            
                                item.setOptions({
                                    minTime: ct.getTime() > new Date ? false : 0,
                                    allowTimes: allowTimes,
                                });
                            } else {
                                item.setOptions({
                                    minTime: ct.getTime() > new Date ? false : 0,
                                    allowTimes: ['9:00', '10:00', '11:00', '12:00', '13:00', '14:00', '15:00', '16:00', '17:00', '18:00', '19:00', '20:00', '21:00'],
                                });
                            }  
                        },
                        error: function () {
                        },
                    });
                },                
            })
        });
    </script>
    
